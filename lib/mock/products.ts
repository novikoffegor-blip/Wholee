function getSeedUnit(product: ProductSeed): ProductUnit {
  return product.category === "Сумки" || (product.category === "Аксессуары" && product.subcategory !== "Перчатки")
    ? "шт."
    : "пар";
}

export const products: Product[] = productSeeds.map((product, index) => {
  const unit = getSeedUnit(product);
  const packSize = unit === "пар" ? 2 : product.category === "Сумки" ? 2 : 4;

  return {
    ...product,
    unit,
    orderStep: packSize,
    packSize,
    stock: product.moq + packSize * (8 + index % 9)
  };
});
